import sys
import re

s = sys.stdin.readline().rstrip('\n')

# 정규식 설명:
# r'<[^>]+>|\s+|[^<>\s]+'
# ─────────────────────────────────────────────
# ① <[^>]+>   :  '<'로 시작해서 '>'가 나오기 전까지의 모든 문자 (태그 전체)
#                 → [^>]는 '>'가 아닌 문자 하나, +는 1개 이상
#                 → 예: <abc>, <online judge>
#
# ② |         :  OR (또는)
#
# ③ \s+       :  하나 이상의 공백 문자 (스페이스, 탭, 줄바꿈 등 포함)
#                 → \s는 whitespace(공백) 문자, +는 1개 이상
#                 → 원래 문장에 있던 공백 개수나 종류를 그대로 유지하기 위함
#
# ④ [^<>\s]+  :  '<', '>', 공백이 아닌 문자들의 연속 (즉, 일반 단어)
#                 → 태그도 아니고, 공백도 아닌 “평범한 단어들”
#
# 이 정규식은 "태그 | 공백 | 일반 단어" 세 가지를 모두 구분해서 토큰화함

tokens = re.findall(r'<[^>]+>|\s+|[^<>\s]+', s)

out = []
for t in tokens:
    if t.startswith('<') and t.endswith('>'):
        # 태그는 그대로 유지
        out.append(t)
    elif t.isspace():
        # 공백(스페이스, 탭, 줄바꿈 등)은 그대로 유지
        out.append(t)
    else:
        # 태그 밖의 단어는 뒤집어서 저장
        out.append(t[::-1])

# 모든 토큰을 이어붙여 출력 (공백 포함 원문 복원)
print(''.join(out))